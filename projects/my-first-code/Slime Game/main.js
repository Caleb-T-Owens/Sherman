let worldLogic = {
  currentWorld: 0,
  drawWorld: () => {
    let x = 0;
    let y = 0;
    ctx.fillStyle = "#ffffff"
    world.map.forEach(line => {
      line.forEach(pixel => {
        if (pixel == 1) {
          ctx.fillRect(y, x, 1, 1);
          //console.log("drew pixel " + y + " " + x)
        }
        y++;
      })
      y = 0;
      x++;
    });
    ctx.fillStyle = "#00ff00"
    ctx.fillRect(world.goal[1], world.goal[0], 5, 4);
  },
  goalCheckingInterval: null,
  startGoalChecking: () => {
    goalCheckingInterval = setInterval(worldLogic.checkGoal, 20);
  },
  checkGoal: () => {
    let isInGoal = false
    // We need to look in all the pixels in the location of the goal and then check if those pixels coordinates are equal to the slimes coordinates. Actually, we need to check above three pixels and accross to the left four pixels
    let goalX = world.goal[0] - 4;
    let goalY = world.goal[1] - 3;
    let slimeX = slime.position[0];
    let slimeY = slime.position[1];
    for (x = 0; x != 4; x++) {
      for (y = 0; y != 5; y++) {
        if (goalX + x == slimeX && goalY + y == slimeY) {
          isInGoal = true;
          break;
        };
      };
      if (isInGoal) {
        break;
      };
    };
    if (isInGoal) {
      worldLogic.changeWorld();
    };
  },
  changeWorld: () => {
    worldLogic.currentWorld++;
    world = worlds[worldLogic.currentWorld];
    worldLogic.drawWorld();
    slime.position = world.spawn;
    slime.drawSlime();
  },

};

