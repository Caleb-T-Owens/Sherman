class polygon {
  constructor (x1,x2,x3,y1,y2,y3,z1,z2,z3) {
    this.x1 = x1;
    this.x2 = x2;
    this.x3 = x3;
    this.y1 = y1;
    this.y2 = y2;
    this.y3 = y3;
    this.z1 = z1;
    this.z2 = z2;
    this.z3 = z3;
    this.centerX = (this.x1 + this.x2 + this.x3) / 3;
    this.centerY = (this.y1 + this.y2 + this.y3) / 3;
    this.centerZ = (this.z1 + this.z2 + this.z3) / 3;
    console.log("New Class made!");
  }
  logCenter () {
    console.log(this.centerX + " " + this.centerY + " " + this.centerZ);
  };
};

